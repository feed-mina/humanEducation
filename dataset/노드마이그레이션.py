def load_overall_progress():
    """JSON 파일에서 전체 진행 상황을 로드합니다.
    각 데이터 타입의 처리된 항목 ID들을 집합(set) 형태로 반환합니다.
    """
    if os.path.exists(progress_file_path):
        with open(progress_file_path, 'r', encoding='utf-8') as f:
            progress_data = json.load(f)

        # 리스트를 집합(set)으로 변환하여 효율적인 조회를 가능하게 합니다.
        processed_state = {}
