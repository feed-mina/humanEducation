df_consume = pd.read_csv("/content/drive/MyDrive/kride/national_travel_consume.csv", encoding="utf-8-sig")
display(df_consume.head())

# sido_enc → sido_name 역매핑 (consume_meta_v3.json에서 가져오기)
with open("/content/drive/MyDrive/kride/consume_meta_v3.json", encoding="utf-8") as f:
    meta = json.load(f)
enc_to_name = {v: k for k, v in meta.get("sido_name_to_enc", {}).items()}

# 시도별 / 계절별 소비 통계
df_consume["cost_krw"] = np.expm1(df_consume["log_one_cost"])
season_map = {1: "봄", 2: "여름", 3: "가을", 4: "겨울"}

agg = (
    df_consume
    .groupby(["sido_enc", "season"])
    .agg(
        avg_cost=("cost_krw", "mean"),
        median_cost=("cost_krw", "median"),
        count=("cost_krw", "count"),
    )
    .reset_index()
)
agg["sido_name"]    = agg["sido_enc"].map(enc_to_name)
agg["season_label"] = agg["season"].map(season_map)

# cost_tier 계산
q33 = agg["avg_cost"].quantile(0.33)
q66 = agg["avg_cost"].quantile(0.66)
agg["cost_tier"] = agg["avg_cost"].apply(
    lambda x: 3 if x >= q66 else (2 if x >= q33 else 1)
)

print(agg.head(10))

# 진행 상황 로드
overall_progress = load_overall_progress()
processed_spend_profiles_set = overall_progress.get('processed_spend_profiles', set())

# ── SpendProfile 노드 + Region 연결 ──
spend_records = agg.to_dict('records')

print("🚀 SpendProfile 노드 생성 및 Region 연결 중...")
for rec in tqdm(spend_records, desc="Spend Profiles"):
    if pd.isna(rec.get("sido_name")):
        continue

    profile_id = f"{rec['sido_name']}-{rec['season_label']}"
    if profile_id in processed_spend_profiles_set:
        continue # 이미 처리된 항목은 건너뛰기

    run_query("""
        MERGE (r:Region {name: $sido_name})
        CREATE (s:SpendProfile {
            season:          $season_label,
            avg_cost_krw:    toInteger($avg_cost),
            median_cost_krw: toInteger($median_cost),
            cost_tier:       $cost_tier,
            cost_tier_label: CASE $cost_tier
                WHEN 1 THEN '저렴'
                WHEN 2 THEN '보통'
                WHEN 3 THEN '고가'
            END,
            sample_count:    $count
        })
        MERGE (r)-[:HAS_SPEND]->(s)
    """, rec)

    processed_spend_profiles_set.add(profile_id)
    overall_progress['processed_spend_profiles'] = processed_spend_profiles_set
    save_overall_progress(overall_progress)

print(f"✅ SpendProfile 노드 생성 완료")

