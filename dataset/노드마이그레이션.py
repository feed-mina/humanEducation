query = """
MATCH (n:POI)-[r:NEAR_BY]-()
WITH n, count(r) AS density
WHERE density > 10
RETURN n.name AS name, n.address AS address, n.category AS category,
       n.lat AS lat, n.lon AS lon, density
ORDER BY density DESC
LIMIT 100
"""

