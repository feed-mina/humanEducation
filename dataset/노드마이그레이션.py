def load_progress():
    if os.path.exists(progress_file_path):
        with open(progress_file_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
        return set(data.get("processed_nodes", [])), set(data.get("processed_edges", []))
    return set(), set()

# Supabase Upsert 재시도 로직 (네트워크 에러 대응)
@retry(stop=stop_after_attempt(5), wait=wait_exponential(multiplier=1, min=2, max=10))
def supabase_upsert(table, data):
    return supabase.table(table).upsert(data).execute()

def migrate():
    print("📂 JSON 데이터 로드 중...")
    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    nodes, edges = data.get('nodes', []), data.get('edges', [])
    processed_nodes, processed_edges = load_progress()
    print(f"📊 대상: 노드 {len(nodes)}개, 엣지 {len(edges)}개 (기처리: {len(processed_nodes)}/{len(processed_edges)})")

    # 1. Supabase Nodes
    print("\n🚀 [1/4] Supabase Nodes...")
    for node in tqdm(nodes, desc="Nodes"):
        nid = str(node['id'])
        if nid in processed_nodes: continue
        try:
            supabase_upsert("nodes", {"id": nid, "name": node.get('name'), "category": node.get('category'), "community_id": node.get('community', 0), "metadata": node})
            processed_nodes.add(nid)
            if len(processed_nodes) % 100 == 0: save_progress(processed_nodes, processed_edges)
        except Exception as e:
            print(f"Node {nid} error: {e}")
            save_progress(processed_nodes, processed_edges)
            raise

    # 2. Supabase Edges
    print("\n🚀 [2/4] Supabase Edges...")
    for edge in tqdm(edges, desc="Edges"):
        sid, tid = str(edge['source']), str(edge['target'])
        etype = edge.get('type', 'FILMING_AT')
        eid = f"{sid}-{etype}-{tid}"
        if eid in processed_edges: continue
        try:
            supabase_upsert("edges", {"source_id": sid, "target_id": tid, "relation_type": etype, "weight": float(edge.get('weight', 1.0))})
            processed_edges.add(eid)
            if len(processed_edges) % 50 == 0: save_progress(processed_nodes, processed_edges)
        except Exception as e:
            print(f"Edge {eid} error: {e}")
            save_progress(processed_nodes, processed_edges)
            raise

    # Neo4j 적재 (일관성을 위해 nodes 리스트 재순회)
    with neo4j_driver.session() as session:
        print("\n🚀 [3/4] Neo4j Nodes...")
        for node in tqdm(nodes):
            label = node.get('type', 'Node')
            session.run(f"MERGE (n:{label} {{id: $id}}) SET n += $props", id=str(node['id']), props={k:v for k,v in node.items() if k not in ['id', 'type']})

        print("\n🚀 [4/4] Neo4j Edges...")
        for edge in tqdm(edges):
            session.run("MATCH (a {id: $s}), (b {id: $t}) MERGE (a)-[r:"+edge.get('type','FILMING_AT')+"]->(b) SET r.weight = $w", s=str(edge['source']), t=str(edge['target']), w=float(edge.get('weight', 1.0)))

    save_progress(processed_nodes, processed_edges)
    print("\n✨ 마이그레이션 완료!")

if __name__ == "__main__":
    try:
        migrate()
    finally:
        neo4j_driver.close()

