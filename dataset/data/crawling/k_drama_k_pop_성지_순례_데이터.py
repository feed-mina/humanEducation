import re

def get_coords_refined(query, drama_name):
    """
    SNS 텍스트에서 드라마 제목 및 불필요한 수식어를 제거하고 장소명 위주로 검색
    """
    if not query: return None, None, None

    # 1. 드라마 제목 및 주요 키워드 제거
    clean_query = query.replace(drama_name, "").replace("촬영지", "").replace("성지순례", "").strip()

    # 2. 이모지 및 특수문자 제거
    clean_query = re.sub(r'[^\w\s]', ' ', clean_query)

    # 3. 괄호 내용 제거 및 공백 정제
    clean_query = re.sub(r'\(.*?\)', '', clean_query)

    # 4. 너무 긴 문장은 앞의 3단어 정도만 사용 (보통 '장소명'이 앞에 옴)
    parts = clean_query.split()
    if not parts: return None, None, None

    search_term = " ".join(parts[:3])

    headers = {"Authorization": f"KakaoAK {KAKAO_API_KEY}"}
    url = f"https://dapi.kakao.com/v2/local/search/keyword.json?query={search_term}"

    try:
        res = requests.get(url, headers=headers, timeout=5).json()
        if res.get('documents'):
            place = res['documents'][0]
            return place['y'], place['x'], place['place_name']
    except:
        pass
    return None, None, None

