def get_coords(query):
    """장소명에서 핵심 키워드만 추출하여 좌표 검색 (개선 버전)"""
    if not query or len(query) < 2:
        return None, None, None

    # 1차 정제: 괄호 및 특수문자 제거, 불필요한 서술어 제거
    # 예: "선재가 살던 집 (수원시 팔달구)" -> "선재가 살던 집"
    clean_query = re.sub(r'\(.*\)', '', query)
    clean_query = re.sub(r'[^\w\s]', ' ', clean_query).strip()

    # 너무 긴 문장은 카카오 API가 인식 못하므로 앞의 2~3단어만 추출
    # 보통 "장소명 - 상세설명" 구조이므로 앞부분이 중요함
    short_query = " ".join(clean_query.split()[:3])

    headers = {"Authorization": f"KakaoAK {KAKAO_API_KEY}"}

    # 시도 1: 정제된 짧은 쿼리로 검색
    url = f"https://dapi.kakao.com/v2/local/search/keyword.json?query={short_query}"

    try:
        res = requests.get(url, headers=headers, timeout=5).json()

        # 결과가 없으면 시도 2: 더 짧게 (앞의 2단어만)
        if not res.get('documents') or len(res['documents']) == 0:
            shorter_query = " ".join(clean_query.split()[:2])
            url = f"https://dapi.kakao.com/v2/local/search/keyword.json?query={shorter_query}"
            res = requests.get(url, headers=headers, timeout=5).json()

        if res.get('documents'):
            place = res['documents'][0]
            # 신뢰도를 높이기 위해 '장소명'이나 '주소'가 포함되어 있는지 확인 가능
            return place['y'], place['x'], place['place_name']

    except Exception as e:
        print(f"Error searching {query}: {e}")

    return None, None, None

